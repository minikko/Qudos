// re:target
var isMobile = /iPhone|iPad|iPod|Android/i.test(navigator.userAgent);
var element = document.getElementById('goto__link1');
if (isMobile) {
    element.href = "https://appcustomer.ridequdos.com/alerts";
} else {
    element.href = "https://ridequdos.com/riderlogin.html";
}

